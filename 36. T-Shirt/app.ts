// Define the function make_shirt
function make_shirt(size: string, message: string): void {
    console.log(`T-Shirt Size: ${size}`);
    console.log(`Message: ${message}`);
    console.log(`Summary: A ${size}-sized shirt with the message "${message}" will be printed.`);
}

// Call the function with specific size and message
make_shirt("Large", "Keep Coding!");

// Call the function with different size and message
make_shirt("Medium", "Stay Awesome!");
